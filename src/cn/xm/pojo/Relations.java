package cn.xm.pojo;

public class Relations {
	
	private Integer r_id;
	
	private Integer r_number;
	
	private String r_name;
	
	private String r_category;

	public Integer getR_id() {
		return r_id;
	}

	public void setR_id(Integer r_id) {
		this.r_id = r_id;
	}

	public Integer getR_number() {
		return r_number;
	}

	public void setR_number(Integer r_number) {
		this.r_number = r_number;
	}

	public String getR_name() {
		return r_name;
	}

	public void setR_name(String r_name) {
		this.r_name = r_name;
	}

	public String getR_category() {
		return r_category;
	}

	public void setR_category(String r_category) {
		this.r_category = r_category;
	}
	
	
}
